  mviewer.customLayers.ocs56.style1 = function (feature, resolution) {
    var level = "";
    var ref = "";
    var val = feature.get("couverture");
    if (resolution > 76) {
      level = "a";
      ref = val.substring(0, 3) + "%";
    } else if (resolution > 38) {
      level = "b";
      ref = val.substr(0, 3) + "?" + val.substr(4, 1) + "%";
    } else if (resolution > 9) {
      level = "c";
      ref = val.substr(0, 3) + "?" + val.substr(4, 1) + "?" + val.substr(6, 1) + "%";
    } else if (resolution > 2) {
      level = "d";
      ref = val;
    }

    feature.getProperties()["label"] =
      mviewer.customLayers.ocs56.classification_couverture[level][ref].title;
    var style = new ol.style.Style({
      fill: new ol.style.Fill({
        color: mviewer.customLayers.ocs56.classification_couverture[level][ref].fill,
      }),
      stroke: new ol.style.Stroke({
        color: mviewer.customLayers.ocs56.classification_couverture[level][ref].stroke,
        width:
          mviewer.customLayers.ocs56.classification_couverture[level][ref].strokewidth +
          0.001,
      }),
    });
    return style;
  };

  mviewer.customLayers.ocs56.style0 = function (feature, resolution) {
    var level = "";
    var ref = "";
    var val = feature.get("usage");
    if (resolution > 76) {
      level = "a";
      ref = val.substring(0, 3) + "%";
    } else if (resolution > 38) {
      level = "b";
      if (val !== "US2.123") {
        ref = val.substr(0, 3) + "?" + val.substr(4, 1) + "%";
      } else {
        ref = val;
      }
    } else if (resolution > 2) {
      level = "c";
      ref = val;
    }
    var style;
    if (mviewer.customLayers.ocs56.classification_usage[level][ref]) {
      feature.getProperties()["label"] =
        mviewer.customLayers.ocs56.classification_usage[level][ref].title;
      style = new ol.style.Style({
        fill: new ol.style.Fill({
          color: mviewer.customLayers.ocs56.classification_usage[level][ref].fill,
        }),
        stroke: new ol.style.Stroke({
          color: mviewer.customLayers.ocs56.classification_usage[level][ref].fill,
          width: 0,
        }),
      });
    } else {
      console.log(ref, val);
      style = new ol.style.Style();
    }
    return style;
  };

